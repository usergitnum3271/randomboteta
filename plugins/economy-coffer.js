function formatTime(totalSec) {
const h = Math.floor(totalSec / 3600)
const m = Math.floor((totalSec % 3600) / 60)
const s = totalSec % 60
const txt = []
if (h > 0) txt.push(`${h} ساعة`)
if (m > 0 || h > 0) txt.push(`${m} دقيقة`)
txt.push(`${s} ثانية`)
return txt.join(' ')
}
