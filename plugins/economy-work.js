function formatTime(ms) {
const totalSec = Math.ceil(ms / 1000)
const minutes = Math.floor((totalSec % 3600) / 60)
const seconds = totalSec % 60
const parts = []
if (minutes > 0) parts.push(`${minutes} دقيقة${minutes !== 1 ? 's' : ''}`)
parts.push(`${seconds} ثانية`)
return parts.join(' ')
}
